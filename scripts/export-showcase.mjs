import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
const GENERATED_DIR = path.join(ROOT, "data", "generated");
const OUT_DIR = path.join(ROOT, "deploy");

async function latestPipeline() {
  const files = await fs.readdir(GENERATED_DIR);
  const pipelineFiles = files.filter((name) => name.endsWith("-pipeline.json"));
  if (!pipelineFiles.length) {
    throw new Error("No pipeline artifacts found. Run /api/pipeline first.");
  }

  const withTime = await Promise.all(
    pipelineFiles.map(async (name) => {
      const full = path.join(GENERATED_DIR, name);
      const stat = await fs.stat(full);
      return { name, full, mtimeMs: stat.mtimeMs };
    })
  );

  withTime.sort((a, b) => b.mtimeMs - a.mtimeMs);
  return withTime[0].full;
}

function asFileName(urlPath) {
  return urlPath.replace(/^\/generated\//, "");
}

function esc(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

async function copyAsset(urlPath) {
  if (!urlPath) {
    return null;
  }

  const name = asFileName(urlPath);
  const src = path.join(GENERATED_DIR, name);
  const dest = path.join(OUT_DIR, name);
  await fs.copyFile(src, dest);
  return name;
}

async function main() {
  const pipelineFile = await latestPipeline();
  const raw = await fs.readFile(pipelineFile, "utf8");
  const summary = JSON.parse(raw);

  await fs.rm(OUT_DIR, { recursive: true, force: true });
  await fs.mkdir(OUT_DIR, { recursive: true });

  const imageFile = await copyAsset(summary.image?.url);
  const jingleFile = await copyAsset(summary.jingle?.url);
  const narrationFile = await copyAsset(summary.narration?.url);

  const reportFileName = asFileName(summary.report?.reportUrl || "");
  const reportText = reportFileName ? await fs.readFile(path.join(GENERATED_DIR, reportFileName), "utf8") : "";

  const html = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1.0" />
    <title>${esc(summary.company)} | Opportunity Beta</title>
    <link rel="preconnect" href="https://fonts.googleapis.com" />
    <link rel="preconnect" href="https://fonts.gstatic.com" crossorigin />
    <link href="https://fonts.googleapis.com/css2?family=Space+Grotesk:wght@400;500;700&family=Fraunces:opsz,wght@9..144,600;9..144,700&display=swap" rel="stylesheet" />
    <style>
      :root { --bg:#081826; --ink:#edf5ff; --muted:#9bb0c7; --accent:#34d399; }
      * { box-sizing:border-box; }
      body { margin:0; font-family:"Space Grotesk",sans-serif; color:var(--ink); background:radial-gradient(circle at 20% 20%, #1a3a52, #060f1a 65%); }
      .hero { position:relative; min-height:42vh; }
      .hero img { width:100%; height:42vh; object-fit:cover; display:block; }
      .overlay { position:absolute; inset:0; display:grid; place-content:center; text-align:center; background:linear-gradient(0deg, rgba(2,8,14,.72), rgba(2,8,14,.35)); }
      h1,h2,h3 { font-family:"Fraunces",serif; margin:0; }
      .overlay h1 { font-size:clamp(1.6rem, 4vw, 3rem); }
      .overlay p { margin:8px 0 0; color:#c8dbef; }
      .wrap { max-width:920px; margin:0 auto; padding:18px; }
      .actions { display:flex; gap:10px; margin-bottom:14px; flex-wrap:wrap; }
      button { border:none; border-radius:999px; padding:10px 14px; font-weight:700; background:linear-gradient(135deg,var(--accent),#10b981); color:#062018; cursor:pointer; }
      .block { background:rgba(255,255,255,.04); border:1px solid rgba(255,255,255,.14); border-radius:12px; padding:14px; margin-bottom:12px; }
      .block p, .block li, pre { color:#d4e3f2; line-height:1.5; }
      pre { white-space:pre-wrap; margin:0; font-size:.9rem; }
      ul { margin:10px 0 0; padding-left:20px; }
      audio { width:100%; margin-top:8px; }
    </style>
  </head>
  <body>
    <header class="hero">
      <img src="./${esc(imageFile)}" alt="Hero" />
      <div class="overlay">
        <h1>${esc(summary.company)} Opportunity Report</h1>
        <p>Generated by Frank Sharpe</p>
      </div>
    </header>
    <main class="wrap" id="story">
      <div class="actions">
        <button id="start">Start Presentation</button>
      </div>
      <section class="block">
        <h2>Why Hire Frank Sharpe</h2>
        <p>${esc(summary.motivation)}</p>
      </section>
      <section class="block">
        <h2>Automation Stack</h2>
        <ul>
          <li>Stable Horde for visuals</li>
          <li>Sonauto for jingle generation</li>
          <li>Local Maximilian voice narration (${esc(summary.voice?.voice || "M2")})</li>
          <li>Interactive story presentation + auto-scroll</li>
          <li>Brevo for direct delivery</li>
          <li>GitHub + Vercel for publication</li>
        </ul>
      </section>
      <section class="block">
        <h2>Report</h2>
        <pre>${esc(reportText)}</pre>
      </section>
      <section class="block">
        <h3>Jingle</h3>
        <audio id="jingle" controls src="./${esc(jingleFile)}"></audio>
        <h3>Narration</h3>
        <audio id="narration" controls src="./${esc(narrationFile)}"></audio>
      </section>
    </main>
    <script>
      const start = document.getElementById("start");
      const story = document.getElementById("story");
      const jingle = document.getElementById("jingle");
      const narration = document.getElementById("narration");
      let timer;

      function autoScroll() {
        clearInterval(timer);
        story.scrollTop = 0;
        timer = setInterval(() => {
          story.scrollTop += 1.15;
          if (story.scrollTop + story.clientHeight >= story.scrollHeight - 2) clearInterval(timer);
        }, 24);
      }

      start.addEventListener("click", async () => {
        autoScroll();
        try {
          if (jingle?.src) {
            jingle.currentTime = 0;
            await jingle.play();
            await new Promise((resolve) => { jingle.onended = resolve; jingle.onerror = resolve; });
          }
          if (narration?.src) {
            narration.currentTime = 0;
            await narration.play();
          }
        } catch {
          // user gesture required on some browsers
        }
      });
    </script>
  </body>
</html>`;

  await fs.writeFile(path.join(OUT_DIR, "index.html"), html, "utf8");

  const vercelJson = {
    cleanUrls: true,
    trailingSlash: false,
    rewrites: [{ source: "/(.*)", destination: "/index.html" }]
  };

  await fs.writeFile(path.join(OUT_DIR, "vercel.json"), JSON.stringify(vercelJson, null, 2), "utf8");

  console.log(`showcase_ready=${OUT_DIR}`);
  console.log(`pipeline_source=${pipelineFile}`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
