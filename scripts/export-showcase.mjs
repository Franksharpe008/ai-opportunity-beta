import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const ROOT = path.resolve(path.dirname(__filename), "..");
const GENERATED_DIR = path.join(ROOT, "data", "generated");
const OUT_DIR = path.join(ROOT, "deploy");

async function latestPipeline() {
  const files = await fs.readdir(GENERATED_DIR);
  const pipelineFiles = files.filter((name) => name.endsWith("-pipeline.json"));
  if (!pipelineFiles.length) {
    throw new Error("No pipeline artifacts found. Run /api/pipeline first.");
  }

  const withTime = await Promise.all(
    pipelineFiles.map(async (name) => {
      const full = path.join(GENERATED_DIR, name);
      const stat = await fs.stat(full);
      return { name, full, mtimeMs: stat.mtimeMs };
    })
  );

  withTime.sort((a, b) => b.mtimeMs - a.mtimeMs);
  return withTime[0].full;
}

function asFileName(urlPath) {
  return String(urlPath || "").replace(/^\/generated\//, "");
}

function esc(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

async function copyAsset(urlPath) {
  if (!urlPath) {
    return null;
  }

  const name = asFileName(urlPath);
  const src = path.join(GENERATED_DIR, name);
  const dest = path.join(OUT_DIR, name);
  await fs.copyFile(src, dest);
  return name;
}

function toListItems(items) {
  return (items || []).map((item) => `<li>${esc(item)}</li>`).join("\n");
}

